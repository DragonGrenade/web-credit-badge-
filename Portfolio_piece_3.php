  <div class="flex-box_container_challenges">
    <!-- First Column -->
    <div class="challenges-left">
      <div class="challenge">
      <h3>Poor Online Booking Experience</h3>
        <p>Users are redirected to an irrelevant third-party platform that feels disconnected from the theater brand.</p>
        <p>Some users cannot book tickets due to technical issues or complex flows they face with the linked booking platform.</p>
      </div>
      <div class="challenge">
      <h3>High Bounce Rate</h3>
        <p>Visitors leave within seconds due to slow loading, poor layout and insufficient content.</p>
       </div>
      <div class="challenge">
        <h3>Loss of Trust & Brand Disconnect</h3>
        <p>The third-party platform design doesn’t reflect the cinema's tone, story, or branding—users feel lost or unsure.</p>
        <p>Mismatched fonts, inconsistent colors, or poor-quality images degrade perceived professionalism.</p>
      </div>
      <div class="challenge">
        <h3>No Involvement or Community Feeling</h3>
        <p>No voting systems or polls for upcoming movies—missing an opportunity for audience engagement.</p>
        <p>No behind-the-scenes content, cast interviews, or special local promotions.</p>
      </div>
      <div class="challenge">
        <h3>No Showcasing of Facilities or Tech</h3>
        <p>No mention of auditoriums, seating comfort, sound systems (e.g., Dolby Atmos), screen quality, or special rooms (VIP, kids, etc.).</p>
        <p>Also, people do not understand how the technology will affect their experience. Missed opportunity to create desire or set expectations visually.</p>
      </div>
    </div>

    <!-- Second Column -->
    <div class="challenges-right">
      <div class="challenge">
        <h3>Negative Testimonials & Reviews</h3>
        <p>As a result of people avoiding to use online booking due to distrust or unfamiliarity, there is frustration due to long waiting lines in person that could’ve been avoided with smoother online booking since more people would book online.</p>
        <p>Users sometimes blame the theater directly even if the fault lies with the third-party service for errors and inaccessibility around the booking platform.</p>
      </div>
      <div class="challenge">
        <h3>Lack of Real-Time Information</h3>
        <p>No clear, live updates on available seats, upcoming movies, or sold-out sessions.</p>
        <p>No notification system for booking confirmations, cancellations, or changes.</p>
      </div>
      <div class="challenge">
        <h3>Lack of Accessibility & Inclusivity</h3>
        <p>No accessible design (for screen readers, visually impaired, etc.).</p>
        <p>No clear info about subtitled movies, audio-described showings, or wheelchair-accessible seating.</p>
      </div>
      <div class="challenge">
        <h3>Poor “About Us” Page</h3>
        <p>No communication of the mission or their evolution.</p>
